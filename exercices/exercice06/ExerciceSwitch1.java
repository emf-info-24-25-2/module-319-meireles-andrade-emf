public class ExerciceSwitch1 {

    public static void main(String[] args) {
        
        int noteModule = 5;

        if (noteModule == 1) {

            System.out.println("Travail non rendu");
            
        } else if (noteModule == 2) {

            System.out.println("Très insuffisant");  

        } else if (noteModule == 3) {
            
            System.out.println("Insuffisant");
        
        } else if (noteModule == 4) {
            
            System.out.println("Suffisant");
        } else if (noteModule == 5) {

            System.out.println("Bien");
            
        } else if (noteModule == 6) {

            System.out.println("Ttès bien");
            
        }
    }
}